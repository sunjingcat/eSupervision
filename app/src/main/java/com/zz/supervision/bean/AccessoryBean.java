package com.zz.supervision.bean;

public class AccessoryBean {
    int accessoryType;
    String accessoryTypeText;
    String id;
    String deviceId;
    String accessoryExplain;
    String accessoryName;

    public String getId() {
        return id;
    }

    public String getDeviceId() {
        return deviceId;
    }

    public String getAccessoryName() {
        return accessoryName;
    }

    public int getAccessoryType() {
        return accessoryType;
    }

    public String getAccessoryTypeText() {
        return accessoryTypeText;
    }

    public String getAccessoryExplain() {
        return accessoryExplain;
    }
}
